"""
Tipos int e float
int -> numero inteiro
o tipo int representa qualquer numero
Positivo ou negativo. int sem sinal é considerado positivo

float -> numero com ponto flutuante
O tipo float representa qualquer numero
Positivo ou negativo com ponto flutuante
float sem sinal é cosiderado positivo

A função type mostra a função que o python inferiu ao valor.
"""

#int
print(11)
print(-11)
print(0)

#float
print(1.1)
print(0.0)
print(-1.5)

#type

print(type('kaiki'))
