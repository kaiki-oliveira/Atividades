'''
python = linguagem de programação
tipo de triagem = dinamica / forte
str -> string -> texto
strings são testos que estão dentro de aspas

 '''

# Aspas simples
print('kaiki oliveira')
print('Kaiki "oliveira"')
 
# Aspas duplas
print("Kaiki Oliveira")
print("Kaiki 'Oliveira'")

# Escape
print("Kaiki \"oliveira\"")

# r
print(r"Kaiki \"oliveira\"")